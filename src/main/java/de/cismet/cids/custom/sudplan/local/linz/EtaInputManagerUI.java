    protected void init()
    {
        try {
            final EtaInput etaInput = inputManager.getUR();
            DefaultTableModel ctoTableModel = (DefaultTableModel)this.etaTable.getModel();
            for(EtaInput.EtaConfiguration etaConfiguration:etaInput.getEtaConfigurations().values())
            {
                ctoTableModel.addRow(new Object[]{
                etaConfiguration.getName(), etaConfiguration.isEnabled(), 
                etaConfiguration.getSedimentationEfficency()});
            }
        } catch (IOException ex) {
             LOG.error("cannot initialise eta input manager ui", ex); // NOI18N
        }
    }
    

