    @Override
    public void progress(final ProgressEvent event) {
        switch (event.getState()) {
            case STARTED: {
                jpbProgress.setValue(0);
                jpbProgress.setString("Creating layer...");
                jpbProgress.setIndeterminate(true);

                dialog.setContentPane(this);
                dialog.pack();
                dialog.setVisible(true);
                break;
            }
            case PROGRESSING: {
                // not interested in progressing
                break;
            }
            case FINISHED: {
                dialog.setVisible(false);
                dialog.dispose();
                break;
            }
        }
    }
